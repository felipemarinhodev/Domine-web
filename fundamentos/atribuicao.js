const a = 7
let b = 3

b += a // atribuição aditiva | b = b + a
console.log(b);

b -= 4 // atribuição subtrativa | b = b - 4
console.log(b);

b *= 2 // atribuição multiplicativa | b = b * 2
console.log(b);

b /= 2 // atribução divisiva | b = b / 2
console.log(b);

b %= 2 // b = b % 2
console.log(b);